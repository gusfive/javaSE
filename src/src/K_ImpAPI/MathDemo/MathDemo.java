package K_ImpAPI.MathDemo;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.abs(-1));//绝对值
        System.out.println(Math.ceil(0.3));//向上取整
        System.out.println(Math.floor(0.9));//向下取整
        System.out.println(Math.round(0.49));//四舍五入
        System.out.println(Math.max(5, 13));//最大值
        System.out.println(Math.pow(2, 3));//次幂
        System.out.println(Math.random());//返回范围为[0.0，1.0）的随机值
    }
}
